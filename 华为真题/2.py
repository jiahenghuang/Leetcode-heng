# void arithmetic(const char * pInputStr, long lInputLen, char * pOutputStr);
# 【输入】
# pInputStr：输入字符串
# lInputLen：输入字符串长度

# 【输出】
# pOutputStr：输出字符串，空间已经开辟好，与输入字符串等长；

# 【注意】
# 只需要完成该函数功能算法，中间不需要有任何IO的输入输出

